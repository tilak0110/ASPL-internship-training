var myVariable = "This is a variable!";
console.log(myVariable); // "This is a variable!"

var number1 = 5;
number1 = 3;

console.log(number1); // 3
alert(number1);       // shows alert with 3

number1 = number1 + 5;      // 3 + 5 = 8
number1 = number1 - 6;      // 8 - 6 = 2

var number2 = number1 * 10; // 2 * 10 = 20
var number3 = number2 / number1; // 20 / 2 = 10

console.log(number2); // 20
console.log(number3); // 10

var myString = "I am a " + "string!";
console.log(myString); // "I am a string!"
