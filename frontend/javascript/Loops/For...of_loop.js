/* 
    - Used for iterating iterables like arrays, strings, maps, sets.
    - useful for Working with strings
*/

//Example 1
const fruits = ["apple", "banana", "cherry"];

for (const fruit of fruits) {
  console.log(fruit);
}
// apple, banana, cherry

